export function getSimilarMovies(movie: Movie, count = 6): Movie[] {
  return MOVIES.filter((m) => m.id !== movie.id).
  map((m) => ({
    movie: m,
    score: m.genres.filter((g) => movie.genres.includes(g)).length
  })).
  sort((a, b) => b.score - a.score).
  slice(0, count).
  map((item) => item.movie);
}

export function getTrendingMovies(count = 10): Movie[] {
  return [...MOVIES].sort((a, b) => b.popularity - a.popularity).slice(0, count);
}

export function getTopRatedMovies(count = 10): Movie[] {
  return [...MOVIES].
  sort((a, b) => b.rating * Math.log10(b.voteCount) - a.rating * Math.log10(a.voteCount)).
  slice(0, count);
}

export function getMoviesByGenre(genre: string, count = 10): Movie[] {
  return MOVIES.filter((m) => m.genres.includes(genre)).slice(0, count);
}

export function getNewReleases(count = 10): Movie[] {
  return [...MOVIES].sort((a, b) => b.year - a.year).slice(0, count);
}